// Imprimir o quadrado dos números de 1 até 20.

package EstruturaRepeticao;


public class exercicioDoze {

    public static void main(String[] args) {

        for (int i = 1; i <= 20; i++) {
            double square = Math.pow(i, 2);
            System.out.println(square);
        }

    }
}
